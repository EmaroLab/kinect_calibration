function [ T ] = quaternionToHomogenuesTrasnformation( t, q ) % t= [x,y,z]' q = [x, y, z, w]
    % get the rotation matrix from q
    q = [q(4) q(1) q(2) q(3)]; % [w x y z]
    R = [ 1 - 2*q(3).^2 - 2*q(4).^2,  ...
        2*q(2)*q(3) - 2*q(1)*q(4), ...
        2*q(4)*q(2) + 2*q(1)*q(3); ...
        ...
        2*q(2)*q(3) + 2*q(1)*q(4), ...
        1 - 2*q(2).^2 - 2*q(4).^2, ...
        2*q(3)*q(4) - 2*q(1)*q(2); ...
        ...
        2*q(4)*q(2) - 2*q(1)*q(3), ...
        2*q(3)*q(4) + 2*q(1)*q(2), ...
        1 - 2*q(2).^2 - 2*q(3).^2 ];

    % show input value
    T = [R, t; 0 0 0 1];
end

